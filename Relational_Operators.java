public class Relational_Operators {
    public static void main(String[]args)
    {
        int a = 47;
        int b= 23;

        // less than
        boolean Less = a<b;
        System.out.println(Less);

        // Greater Than
        boolean Greater = a>b;
        System.out.println(Greater);

        // equals too
        boolean equ = a==b;
        System.out.println(equ);

        //less than equals to
        boolean Lessequ = a<=b;
        System.out.println(Lessequ);

        // Greater than equals to
        boolean Greequ = a>=b;
        System.out.println(Greequ);

        // Not equals to
        boolean Not = a!=b;
        System.out.println(Not);

    }
    
}
