public class Arithematic_operations {
    public static void main(String[] args) {
        int a = 25;
        int b =120;

        // Addition 
        int result1 = a+b;
        System.out.println(result1);

        //Subtract
        int sub = a-b;
        System.out.println(sub);

        //Multiplication
        int multi = a*b;
        System.out.println(multi);

        // Division
        int Div = a/b;
        System.out.println(Div);

        // Module
        int mod = a%b;
        System.out.println(mod);
    }
    
}
